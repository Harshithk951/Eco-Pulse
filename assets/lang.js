function applyLang(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) {
      el.textContent = TRANSLATIONS[lang][key];
    }
  });

  // Update toggle button styles on every page
  const btnEn = document.getElementById('btn-en');
  const btnHi = document.getElementById('btn-hi');
  if (btnEn && btnHi) {
    const active = "px-6 py-2 rounded-full text-sm font-bold transition-all bg-primary-container text-on-primary";
    const inactive = "px-6 py-2 rounded-full text-sm font-bold transition-all text-on-surface-variant hover:bg-surface-container-high";
    btnEn.className = lang === 'en' ? active : inactive;
    btnHi.className = lang === 'hi' ? active : inactive;
  }
}

function switchLang(lang) {
  localStorage.setItem('eco_lang', lang);
  applyLang(lang);
}

// Auto-apply on every page load
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('eco_lang') || 'en';
  applyLang(saved);
});
